int recursive_(int altitude[][MAP_MAX], int size, int top[2], int bot[2], int ref[2]){
    int area = (bot[0] - top[0] + 1) * (bot[1] - top[1] + 1), tmp = 0;
    bool rec_start = true;
    //left pump
    if(top[1]-1 >= 0){
        for(int i = top[0]; i <= bot[0]; i++){
            if(altitude[i][top[1]-1] >= altitude[ref[0]][ref[1]]){
                rec_start = false;
                break;
            }
        }
        int arr[2] = {top[0], top[1]-1};
        if(rec_start) {
            tmp = recursive_(altitude, size, arr, bot, ref);
        }
    }
    if(tmp > area)
        area = tmp;
    rec_start = true;
    //up pump
    if(top[0]-1 >= 0) {
        for (int j = top[1]; j <= bot[1]; j++) {
            if(altitude[top[0]-1][j] >= altitude[ref[0]][ref[1]]) {
                rec_start = false;
                break;
            }
        }
        int arr[2] = { top[0]-1, top[1] };
        if(rec_start)
            tmp = recursive_(altitude, size, arr, bot, ref);
    }
    if(tmp > area)
        area = tmp;
    rec_start = true;
    //right pump
    if((bot[1] + 1) < size){
        for(int i = top[0]; i <= bot[0]; i++){
            if(altitude[i][bot[1]+1] >= altitude[ref[0]][ref[1]]) {
                rec_start = false;
                break;
            }
        }
        int arr[2] = {bot[0], bot[1]+1};
        if(rec_start)
            tmp = recursive_(altitude, size, top, arr, ref);
    }
    if(tmp > area)
        area = tmp;
    rec_start = true;
    //down pump
    int arr[2] = {bot[0]+1, bot[1]};
    if((bot[0] + 1) < size){
        for(int j = top[1]; j <= bot[1]; j++){
            if(altitude[bot[0] + 1][j] >= altitude[ref[0]][ref[1]]){
                rec_start = false;
                break;
            }
        }
        if(rec_start) {
            tmp = recursive_(altitude, size, top, arr, ref);
        }
    }
    if(tmp > area)
        area = tmp;
    return area;
}

void castleArea ( int altitude[][MAP_MAX], int size, int area[][MAP_MAX] )
{
    for(int i = 0; i < size; i++){
        for(int j = 0; j < size; j++){
            int arr[2] = {i, j};
            area[i][j] = recursive_(altitude, size, arr , arr, arr);
                 }
    }
}

